                    <p style="padding-top: 2%"><i style="color: white">"En Belgique, comme ailleurs, la cybersécurité est devenue une priorité stratégique pour les organisations, tout secteur confondu. Dans ce contexte, former et engager des jeunes talents en sécurité de l'information est devenu crucial."</i><a href="https://www.cybersecuritychallenge.be/" target="_blank">  - Cybersecuritychallenge.be</a></p>

                    <p style="padding-top: 2%">La cybersécurité est un des nombreux domaines qui me passionnent dans l'informatique.</p>
                    <p>
                        Plus jeune, j'ai regardé des reportages sur la cybercriminalité. J'ai alors commencé à m'intéresser à ce domaine. J'ai alors commencé à faire des petits challenges sur le site <a href="https://www.root-me.org/" target="_blank">root-me</a>.
                        Au fur et à mesure que j'avançais dans les challenges, j'ai appris et acquéris des compétances dans ce domaine.
                        L'Ephec nous a alors proposé ce challenge. J'ai directement accepté avec 2 autres de mes camarades.
                    </p>
                    <p>
                        Le challenge est composé de défis sous forme de "capture the flag" rapportant un certain nombre de points qui permettront, si suffisant, de passer à l'étape suivante.<br>
                        "Capture the flag" est un jeu consistant à exploiter des vulnérabilités affectant des logiciels de manière à s’introduire sur des ordinateurs pour récupérer les drapeaux, preuves de l’intrusion.
                    </p>
                    <p>
                        Nous n'avons malheureusement pas eu suffisamment de temps et d'expérience pour gagner assez de points afin d'atteindre l'étape suivante.
                        Ce challenge m'a permis de mieux gérer le stress lorsqu'il faut réaliser une certaine quantité de travail dans un temps restreint.
                        En effet, nous n'avions qu'une seule journée pour réaliser le plus de défis possibles afin d'être qualifié pour l'étape suivante.<br>
                        J'ai également acquéris de nouvelles connaissances dans ce domaine suite à de nombreuses recherches afin de résoudre ces défis.
