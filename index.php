                    <p style="padding-top: 2%">
                        Après quelques cours d'électronique et plusieurs vidéos sur YouTube sur ce sujet, j'ai commencé à trouver un certain intérêt dans pour ce milieu. J’ai alors acheté un petit starter-kit sur internet pour commencer à me familiariser avec le matériel.
                    </p>
                    <p>
                        Après certains petits projets sans intérêts, il m’est venu à l’idée de faire un projet où la programmation avait une place plus importante. J’ai donc pensé à faire un petit robot. Cependant, celui-ci était trop compliqué au niveau de la mécanique. J’ai eu alors l’idée de faire un robot sur roues. La tâche de ce robot, pour commencer, est de se balader dans un environnement tout en évitant les obstacles afin d’éviter tout endommagement du matériel.
                    </p>
                    <p>
                        J’ai entrepris quelques recherches afin de savoir quel matériel et composants électroniques seraient nécessaires au fonctionnement de ce robot.
                    </p>
                    <p>
                        Après réception des pièces, j’ai testé chacun d’entre elle pour comprendre leur fonctionnement et comment les intégrer dans le programme final.
                    </p>
                    <p>
                        Une fois toutes les pièces maitrisées, je suis passé à l’assemblage du robot. N’étant pas encore bien fournis en outils, j’ai dû redoubler d’ingéniosité pour trouver des solutions alternatives.
                    </p>
                    <p>
                        Vient alors la partie du code. Cette partie fu relativement facile étant donné que chaque pièce avait déjà son code réalisé auparavant.
                    </p>
                    <p>
                        Ensuite, j’ai dû réaliser de nombreux testes et corrections pour que le robot soit 100% fonctionnel.
                    </p>
                    <p>
                        Malgré quelques problèmes liés au manque de matériel, le robot est tout à fait fonctionnel.
                    </p>
                    <p>
                        Ce projet m’a permis de visualiser les différentes étapes à réaliser afin d’arriver à un résultat final fonctionnel dans le domaine de l’électronique.
                    </p>
